    @Override
    public int executeUpdate() throws SQLException {
        checkClosed();
        try {
            result = new CassandraResultSet(this, session.execute(bindParameters()));
            if (result.isQuery()) {
                throw new SQLException("Not an update statement");
            }
            return 1;
        } catch (SyntaxError ex) {
            throw new SQLSyntaxErrorException(ex.getMessage(), ex);
        } catch (Throwable t) {
            throw new SQLException(t.getLocalizedMessage(), t);
        }
    }

    @Override
    public int executeUpdate(String sql) throws SQLException {
        throw new SQLException("Method should not be called on prepared statement");
    }

    @Override
    public void close() {
        super.close();
        if (result != null) {
            result.close();
            result = null;
        }
    }

    @Override
    public ResultSet getResultSet() throws SQLException {
        checkClosed();
        return result;
    }

