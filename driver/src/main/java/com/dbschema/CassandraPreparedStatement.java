    @Override
    public boolean execute() throws SQLException {
        checkClosed();
        try {
            return executeInner(session.execute(bindParameters()));
        } catch (Throwable t) {
            throw new SQLException(t.getMessage(), t);
        }
    }

    private BoundStatement bindParameters() {
        try {
            return preparedStatement.bind(params == null ? new Object[]{} : params);
        } finally {
            clearParams();
        }
    }

    private void clearParams() {
        if (params == null) return;
        for (int i = 0; i < params.length; i++) {
            params[i] = null;
        }
    }

    @Override
    public void addBatch() throws SQLException {
        try {
            if (batchStatement == null) {
                batchStatement = new BatchStatement();
            }
            batchStatement.add(preparedStatement.bind(params == null ? new Object[]{} : params));
        } catch (Throwable t) {
            throw new SQLException(t.getMessage(), t);
        } finally {
            clearParameters();
        }
    }

